show_spinner() {
    local pid=$1
    local delay=0.1
    local spinstr='|/-\'
    while [ "$(ps a | awk '{print $1}' | grep $pid)" ]; do
        local temp=${spinstr#?}
        printf " [%c]  " "$spinstr"
        local spinstr=$temp${spinstr%"$temp"}
        sleep $delay
        printf "\b\b\b\b\b\b"
    done
    printf "    \b\b\b\b"
}

show_progress() {
    local duration=$1
    local step=$((duration/20))
    echo -ne "${CYAN}[                    ] 0%${NC}\r"
    for i in {1..20}; do
        sleep $step
        local progress=$((i*5))
        local bars=$(printf "%${i}s" | tr ' ' '‚ñà')
        local spaces=$(printf "%$((20-i))s" | tr ' ' ' ')
        echo -ne "${CYAN}[${bars}${spaces}] ${progress}%${NC}\r"
